export type AnalysisCategory = 'redundancy' | 'implementation' | 'performance' | 'security';

export interface AnalysisFinding {
	category: AnalysisCategory;
	severity: 'info' | 'warning' | 'critical';
	title: string;
	description: string;
	files: string[];
	lines?: { file: string; start: number; end: number }[];
	impact: string;
	recommendation: string;
}

const CATEGORY_PROMPTS: Record<AnalysisCategory, string> = {
	redundancy: `You are a code redundancy analyst. Analyze the codebase for:
- Duplicate logic — functions or blocks that do the same thing
- Copy-pasted code — near-identical code in multiple places
- Overlapping functions — functions with significant overlap that could be consolidated
- Unnecessary abstractions — wrappers that add no value
- Dead code — unreachable or unused code paths

Focus ONLY on redundancy. Do not report style, performance, or security issues.`,

	implementation: `You are an implementation correctness analyst. Analyze the codebase for:
- Race conditions — shared state without proper synchronization
- Incorrect async patterns — missing await, unhandled promises, callback hell
- Deadlock potential — circular dependencies in async operations
- Wrong data structures — using arrays where sets/maps would be better
- Logic errors — off-by-one, wrong comparisons, missing edge cases
- Resource leaks — unclosed handles, event listeners not cleaned up

Focus ONLY on implementation correctness. Do not report style, redundancy, or security issues.`,

	performance: `You are a performance analyst. Analyze the codebase for:
- N+1 query patterns — database or API calls inside loops
- Blocking operations — synchronous I/O in async contexts, blocking main thread
- Unnecessary computation — repeated calculations, missing memoization
- Large bundle imports — importing entire libraries when only a small part is needed
- Expensive operations in hot paths — heavy processing in frequently-called code
- Memory issues — large object retention, unbounded growth

Focus ONLY on performance. Do not report style, redundancy, or security issues.`,

	security: `You are a security analyst. Analyze the codebase for:
- Injection risks — SQL injection, command injection, XSS, template injection
- Hardcoded secrets — API keys, passwords, tokens in source code
- Insecure defaults — permissive CORS, disabled auth, debug modes in prod config
- Path traversal — unsanitized file paths from user input
- Insecure crypto — weak algorithms, predictable random, missing salt
- Information leakage — stack traces, internal paths, or version info exposed to users

Focus ONLY on security. Do not report style, redundancy, or performance issues.`,
};

