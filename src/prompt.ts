export function buildChunkMessage(files: CollectedFile[]): string {
	const parts: string[] = [
		`Analyze the following ${files.length} source files (chunk of a larger codebase) and extract the engineering patterns:\n`,
	];

	for (const file of files) {
		parts.push(`--- FILE: ${file.relativePath} ---`);
		parts.push(file.content);
		parts.push('');
	}

	return parts.join('\n');
}

export function buildCompilerMessage(chunkOutputs: string[]): string {
	const parts: string[] = [
		`The following are pattern analysis outputs from ${chunkOutputs.length} sub-agents, each analyzing a different chunk of the same codebase. Compile them into a single unified patterns document.\n`,
	];

	for (let i = 0; i < chunkOutputs.length; i++) {
		parts.push(`=== CHUNK ${i + 1} ANALYSIS ===`);
		parts.push(chunkOutputs[i]);
		parts.push('');
	}

	return parts.join('\n');
}

