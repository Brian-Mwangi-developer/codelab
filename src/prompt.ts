export function buildDeepAnalysisPlanMessage(patternsContent: string, files: CollectedFile[]): string {
	const fileList = files.map(f => f.relativePath).join('\n');
	return `## Project patterns:\n\n${patternsContent}\n\n## Files in the codebase (${files.length} total):\n${fileList}`;
}

export function buildDeepAnalysisCategoryMessage(
	category: AnalysisCategory,
	plan: string,
	files: CollectedFile[]
): string {
	const parts: string[] = [
		`## Analysis plan context:\n${plan}\n\n## Source files to analyze for ${category} issues:\n`,
	];

	for (const file of files) {
		parts.push(`--- FILE: ${file.relativePath} ---`);
		parts.push(file.content);
		parts.push('');
	}

	return parts.join('\n');
}

export function buildDeepAnalysisCompilerMessage(categoryOutputs: Record<string, string>): string {
	const parts: string[] = [
		`Compile the following findings from 4 specialist agents into a unified analysis:\n`,
	];

	for (const [category, output] of Object.entries(categoryOutputs)) {
		parts.push(`=== ${category.toUpperCase()} AGENT FINDINGS ===`);
		parts.push(output);
		parts.push('');
	}

	return parts.join('\n');
}
